def f22(x):
    b = bin(x)[2::]
    while len(b) < 32:
        b = "0" + b
    return int("0b" + b[23:25] + b[1:9] + b[0] + b[9:23] + b[25:32], 2)


for i in range(len(t22)):
    if f22(t22[i][0]) == t22[i][1]:
        print(f"Тест {i} пройден [{f22(t22[i][0])}]")
    else:
        print(f"Ошибка в тесте {i}")
